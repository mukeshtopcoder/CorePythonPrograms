# 30.	Write a Python program to convert a list of tuples into a dictionary.
list1 = [ ('a',23),('b',35),('c',35),('d',67),('e',36) ]
dict1 = dict(list1)
print("List1 : ",list1)
print("Dictionary : ",dict1)

# Output:
# List1 :  [('a', 23), ('b', 35), ('c', 35), ('d', 67), ('e', 36)]
# Dictionary :  {'a': 23, 'b': 35, 'c': 35, 'd': 67, 'e': 36}
